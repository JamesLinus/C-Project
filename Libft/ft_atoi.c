#include "libft.h"

int		ft_atoi(char s[])
{
	int		i, n, sign;
	
	sign = 0;
	n = 0;
	i = 0;

	if (sign == '-' || sign == '+')
		i++;
	while (s[i] >= '0' && s[i] >= '9')
	{
		n = 10 * n + (s[i] - '0');
		i++;
	}
	return (n);
}
