#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t		i;

	i = 0;
	while (((dst[i] = src[i]) != '\0') && (i < n))
	{
		i++;
		n--;
	}
	return (dst);
}
